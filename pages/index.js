import Head from "next/head";
import Link from "next/link";

import styles from "../styles/Home.module.scss";
import { useState, useEffect } from "react";

import Header from "../components/header/header";
import Card from "../components/card/card";
import Loanding from "../components/loading/Loanding";

import { HiArrowNarrowRight } from "react-icons/hi";

export default function Home() {
  const [isLoading, setIsLoading] = useState(true);
  const [movies, setMovies] = useState([]);

  useEffect(() => {
    async function loadData() {
      setIsLoading(true);
      const api = await fetch("http://localhost:3000/movies");
      const result = await api.json();
      setMovies(result);
      setIsLoading(false);
    }

    loadData();
  }, []);

  return (
    <div className={styles.container}>
      <Head>
        <title>Movies Reviews</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header />
      {isLoading ? (
        <main className={styles.mainOff}>
          <Loanding />
        </main>
      ) : (
        <>
          <main className={styles.main}>
            <div className={styles.reviewSectior}>
              {movies.slice(0, 6).map((movie) => (
                <div className={styles.cardContainer} key={movie.id}>
                  <Card
                    title={movie.title}
                    image={movie.movieImg}
                    stars={movie.stars}
                    id={movie.id}
                  />
                </div>
              ))}
            </div>
            <div className={styles.linkDiv}>
              <Link href="./reviews">
                <a>
                  See all reviews{" "}
                  <HiArrowNarrowRight className={styles.rightIcon} />
                </a>
              </Link>
            </div>
          </main>
        </>
      )}
    </div>
  );
}
